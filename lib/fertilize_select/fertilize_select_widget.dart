class FertilizeSelectWidget extends StatefulWidget {
  /// System Design Requirements:
  ///
  /// User Inputs:
  ///
  /// Crop Type: Select from:
  /// Vegetable
  /// Fruit
  /// Flower
  /// Growth Stage: Select from:
  /// Seedling
  /// Vegetation
  /// Flowering/Fruiting
  /// Symptoms: Select from:
  /// Yellow Leaves
  /// Stunted Growth
  /// Weak Growth
  /// No Symptom
  /// Area (in acres): Numeric input representing the area of cultivation.
  /// Fertilizer Recommendation Logic:
  ///
  /// Recommendations will be based on N (Nitrogen), P (Phosphorous), and K
  /// (Potassium) values.
  /// Logic table (for all crops and growth stages):
  /// Symptom	Growth Stage	N (Nitrogen)	P (Phosphorous)	K (Potassium)
  /// Yellow Leaves	Seedling	30	10	10
  /// Vegetation	30	20	20
  /// Flowering/Fruiting	20	30	40
  /// Stunted Growth	Seedling	40	20	20
  /// Vegetation	50	30	30
  /// Flowering/Fruiting	30	40	30
  /// Weak Growth	Seedling	20	40	20
  /// Vegetation	20	50	30
  /// Flowering/Fruiting	10	40	50
  /// No Symptom	Seedling	25	15	10
  /// Vegetation	40	30	20
  /// Flowering/Fruiting	15	40	45
  /// Area-Based Calculation:
  ///
  /// Multiply the area (in acres) by 1 liter per acre to compute the total
  /// fertilizer volume required.
  /// Output:
  ///
  /// Display the recommended fertilizer composition (N, P, K values) and the
  /// total volume required for the given area.
  /// Example Output:
  /// Crop Type: Vegetable
  /// Growth Stage: Seedling
  /// Symptom: Yellow Leaves
  /// Recommendation: N:30, P:10, K:10
  /// Total Volume: 5 liters for 5 acres.
  /// Flowchart of the System:
  ///
  /// Start →
  /// Prompt: "What type of crop are you growing?"
  /// Options: Vegetable, Fruit, Flower →
  /// Prompt: "What is the growth stage?"
  /// Options: Seedling, Vegetation, Flowering/Fruiting →
  /// Prompt: "What symptoms are visible?"
  /// Options: Yellow Leaves, Stunted Growth, Weak Growth, No Symptom →
  /// Based on input, fetch fertilizer recommendation (NPK values).
  /// Prompt: "Enter the area (in acres)."
  /// Calculate total fertilizer volume = Area × 1 liter.
  /// Display fertilizer composition and total volume.
  /// End.
  const FertilizeSelectWidget({
    super.key,
    String? nitrogenValue,
  }) : nitrogenValue = nitrogenValue ?? '10';

  final String nitrogenValue;

